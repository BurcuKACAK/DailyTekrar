package JavaQuestionsBank.Variables;

public class Variables_Q07 {
    public static void main(String[] args) {
        /*
        İki tamsayıyı değiştirmek için bir kod yazınız
         */

        int a =8;
        int b =9;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        a=a+b;
        b=a-b;
        a=a-b;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
