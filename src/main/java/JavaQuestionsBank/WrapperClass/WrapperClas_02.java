package JavaQuestionsBank.WrapperClass;

public class WrapperClas_02 {
    public static void main(String[] args) {
          /*
        Byte data tipinde bir değişken oluşturunuz ve onu bir double değişkenine dönüştürünüz.
        Sonrasında bu double değişkenin değerini konsolda yazdırınız.
        Not : Çıktıya dikkat ediniz, ondalık bir değer olacaktır
         */

        byte a = 12;
        double newa = (double) a;
        System.out.println(newa);

        byte age = 23;
        double newAge = age;
        System.out.println(newAge);
    }
}
