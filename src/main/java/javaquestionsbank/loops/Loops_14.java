package javaquestionsbank.loops;

public class Loops_14 {
    public static void main(String[] args) {
        //3 ile 9 arasındaki tam sayıların çarpımını bulmak için kodu yazınız.

        int sum =1;
        for (int i = 3; i <10 ; i++) {
            sum*=i;

        }
        System.out.print(sum);

    }
}
